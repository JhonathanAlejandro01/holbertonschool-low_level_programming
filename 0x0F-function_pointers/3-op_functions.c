#include <stidio.h>
/**
 * op_add - operator add
 *@a: is a integer number
 *@b: is a integer number
 * Return: total
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - operator add
 *@a: is a integer number
 *@b: is a integer number
 * Return: total
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - operator add
 *@a: is a integer number
 *@b: is a integer number
 * Return: total
 */
int op_mul(int a, int b)
{
	return (a * b);
}
